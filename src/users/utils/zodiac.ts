export function getZodiac(input: string | Date): string {
  const date = typeof input === 'string' ? new Date(input) : input;

  for (const zodiac of chineseZodiacs) {
    const start = new Date(zodiac.start);
    const end = new Date(zodiac.end);

    if (date >= start && date <= end) {
      return zodiac.name;
    }
  }
  return 'Unknown';
}
